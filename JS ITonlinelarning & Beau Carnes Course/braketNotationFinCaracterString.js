firstLetter0fFirstName = firstName[0]; // Accesare caracter la index 0
console.log(firstLetter0fFirstName);   // Afișare: "y"

// Exemplul 2 - Nume de familie
var firstLetter0fLasttName = "";  // Variabilă pentru primul caracter
